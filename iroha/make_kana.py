char_color = ( 220, 221, 221)

img = Image.new("RGB", ( block_len_width*4 + 14*13 * CHAR_ROW, block_len_height*4 + 14*13 * CHAR_COL), ( 67, 67, 69))

def write_img( img, offset_row, offset_col, text):
    for x in range(0,matrix_len_width):
        for y in range(0,matrix_len_height):
            if text.split()[y][x] == "o":
                for px in range(block_len_width):
                    for py in range(block_len_height):
                        img.putpixel(
                           (block_len_width*x+px + offset_row,
                            block_len_height*y+py + offset_col),
                            char_color
                        )

count = 0
for v in text:
    write_img(
        img,
        block_len_width*4 + (block_len_width*2 + block_len_width * matrix_len_width) * count,
        block_len_height*2,
        v
    )
    count += 1

img.save("new.png","PNG")
